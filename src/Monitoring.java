    private void buttonsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonsaveActionPerformed
         createMonitoring();
        if(currentMonitoring !=null){
            Monitor.MonitoringList.add(currentMonitoring);
            ModelM.setValueAt(currentMonitoring.getRegisterNumber(),Monitor.MonitoringList.size()-1, 0);
            ModelM.setValueAt(currentMonitoring.getFirstWeek(),Monitor.MonitoringList.size()-1, 1);
            ModelM.setValueAt(currentMonitoring.getSecondWeek(),Monitor.MonitoringList.size()-1, 2);
            ModelM.setValueAt(currentMonitoring.getThirdWeek(),Monitor.MonitoringList.size()-1, 3);
            ModelM.setValueAt(currentMonitoring.getFourthWeek(),Monitor.MonitoringList.size()-1, 4);
        }
        this.dispose();
    }//GEN-LAST:event_buttonsaveActionPerformed

