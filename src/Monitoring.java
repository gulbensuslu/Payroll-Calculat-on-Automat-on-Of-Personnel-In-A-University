    private void createMonitoring(){
       String RegisterNumber = TextFieldRN.getText();
       String FirstWeek = TextFieldFW.getText();
       String SecondWeek = TextFieldSW.getText();
       String ThirdWeek = TextFieldTW.getText();
       String FourthWeek = TextFieldFW.getText();
       boolean RegisterNumberCheck = RegisterNumber.isEmpty();
       boolean FirstWeekCheck = FirstWeek.isEmpty();
       boolean SecondWeekCheck = SecondWeek.isEmpty();
       boolean ThirdWeekCheck = ThirdWeek.isEmpty();
       boolean FourthWeekCheck = FourthWeek.isEmpty();
        if (RegisterNumberCheck) {
            JOptionPane.showMessageDialog(this, "Register Number Field is empty!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        if (FirstWeekCheck) {
            JOptionPane.showMessageDialog(this, "First Week Field is empty!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        if (SecondWeekCheck) {
            JOptionPane.showMessageDialog(this, "Second Week Field is empty!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        if (ThirdWeekCheck) {
            JOptionPane.showMessageDialog(this, "Third Week Field is empty!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
         if (FourthWeekCheck) {
            JOptionPane.showMessageDialog(this, "Fourth Week Field is empty!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        
        if (!RegisterNumberCheck && !FirstWeekCheck && !SecondWeekCheck && !ThirdWeekCheck && !FourthWeekCheck) {
            currentMonitoring= new Monitor(RegisterNumber , FirstWeek, SecondWeek, ThirdWeek,FourthWeek);
            JOptionPane.showMessageDialog(this, "Monitor is created Successfully!", "Sucess!", JOptionPane.INFORMATION_MESSAGE);

        }
    }
